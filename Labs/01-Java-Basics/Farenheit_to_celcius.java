package operators2;

public class Farenheit_to_celcius {
	 public static void main(String[] args) {
	        double fahrenheit = 77.0;
	        double resultCelsius = (fahrenheit - 32) * 5 / 9;

	        System.out.println("Fahrenheit to Celsius: " + fahrenheit + "°F = " + resultCelsius + "°C");
	    }
}
